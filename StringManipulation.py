print("########### String Manipulation in Python ###########")

FirstName = input("Enter your first name: ")
LastName = input("Enter your last name: ")

#Confirmation to the User
print("Given Name: %s %s"%(FirstName, LastName))

#We are printing only first letter in first & last name.
print("Given Name after String Manipulation: %s %s"%(FirstName[0], LastName[0]))
